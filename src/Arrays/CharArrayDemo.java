package Arrays;

public class CharArrayDemo {

    public static void main(String[] args) {
        char[]  a = new char[5];

        a[0] = 'k';
        a[1] = 'b';
        a[2] = 'x';
        a[3] = 'y';
        a[4] = 'z';

        for (int i=0 ; i<5 ;i++ )
           System.out.println(a[i]);
    }
}
