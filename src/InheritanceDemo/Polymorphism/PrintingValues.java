package InheritanceDemo.Polymorphism;

public class PrintingValues {

    public static void main(String[] args) {

        System.out.println("Hello World");

        int a = 10;
        System.out.println(a);

        double d = 11.10;
        System.out.println(d);

        boolean x = true;
        System.out.println(x);
    }
}
